Что касается симуляции роутеров, есть несколько вариантов:
1 Использовать виртуальные машины под Ubuntu/Debian и настроить их как маршрутизаторы с помощью ПО вроде VyOS или Vyatta.

2 Применить функцию внутреннего программного маршрутизатора (Internal Network) 
    в VirtualBox для соединения виртуальных машин.

3 Задействовать режим Bridged Networking в VirtualBox для подключения виртуальных машин к реальной локальной сети вашего компьютера.





